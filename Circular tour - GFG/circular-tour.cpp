class Solution{
  public:
  
    //Function to find starting point where the truck can start to get through
    //the complete circle without exhausting its petrol in between.
    int tour(petrolPump p[],int n)
    {
       //Your code here
       int i=0,j=0;
       int petrol = 0,require=0;
       while(j<n){
           petrol = petrol + p[j].petrol - p[j].distance;
           if(petrol<0){
               require = require - petrol;
               i=j+1;
               petrol = 0;
           }
           j++;
       }
       if(petrol-require>=0){
           return i;
       }
       return -1;
    }
};



