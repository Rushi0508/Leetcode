class Solution{
    public:
        
    // A[]: input array
    // N: size of array
    // Function to find the maximum index difference.
    int maxIndexDiff(int A[], int N) 
    { 
        // Your code here
        int low=0,high=N-1,total=0,diff;
       while(high>=low)
       {
           if(A[high]>=A[low])
           {
               diff=high-low;
               total=max(total,diff);
               low++;
               high=N-1;
           }
           else
           high--;
           
       }
       return total;}
};

