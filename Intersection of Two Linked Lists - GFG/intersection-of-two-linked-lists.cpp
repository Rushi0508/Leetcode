class Solution{
  public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        // code here
        // return the head of intersection list
        unordered_map<int,int> mp;
        while(head2){
            if(head2){
                mp[head2->data]++;
                head2 = head2->next;
            }
        }
        Node* ans = new Node(0);
        Node* curr = ans;
        Node* temp = head1;
        while(temp){
            if(mp[temp->data]){
                curr->next = new Node(temp->data);
                curr = curr->next;
            }
            temp=temp->next;
        }
        return ans->next;
    }
};

